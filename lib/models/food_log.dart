class FoodLog {
  final String name;
  final String imageUrl;
  final int calories;
  final DateTime timestamp;
  final double protein;
  final double carbs;
  final double fat;

  FoodLog({
    required this.name,
    required this.imageUrl,
    required this.calories,
    required this.timestamp,
    required this.protein,
    required this.carbs,
    required this.fat,
  });
}
